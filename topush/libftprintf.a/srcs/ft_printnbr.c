#include "../ft_printf.h"

int	ft_printnbr(int n)
{
	int		i;
	char	*num;

	i = 0;
	if (!n)
		return (0);
	num = ft_itoa(n);
	i = ft_strlen(num);
	free (num);
	return (i);
}
